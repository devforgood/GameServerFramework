
public class GradeData : ACDataSync<int, JGradeData>
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// GradeData()
	//--------------------------------------------------------------------------------------------------
	//	Desc.
	//
	////////////////////////////////////////////////////////////////////////////////////////////////////
	public GradeData( EACDataType eDataType ) 
		: base( eDataType )
	{
	}

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // GetGrade()
    //--------------------------------------------------------------------------------------------------
    //	Desc.
    //		배틀 스코어로 등급 데이터 얻기
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public JGradeData GetGrade(int battleScore)
    {
        JGradeData grade = null;
        foreach (var data in this.Values)
        {
            if (data.AccountBS >= battleScore)
            {
                grade = data;
                break;
            }
        }
        return grade;
    }
}
